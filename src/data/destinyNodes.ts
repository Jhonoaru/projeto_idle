export function getDestinyNodeById(nodeId?: string) {
  return destinyNodes.find((node) => node.id === nodeId);
}

function vocationNodes(
  vocation: Vocation,
  entries: Array<[
    string,
    string,
    string,
    DestinyNode["category"],
    number,
    DestinyNode["position"],
    DestinyNode["bonus"],
  ]>,
): DestinyNode[] {
  return entries.map(([id, name, description, category, requiredLevel, position, bonus], index) => ({
    id,
    name,
    description,
    category,
    shape: index === 2 ? "major" : "medium",
    requiredLevel,
    cost: index === 2 ? 2 : 1,
    prerequisiteNodeIds: index === 0 ? ["destiny-battle-instinct"] : [entries[index - 1][0]],
    allowedVocations: [vocation],
    position,
    bonus,
  }));
}
